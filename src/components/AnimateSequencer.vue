  if (!isStarted.value) {
    Tone.start()
    setStarted()
  }
  togglePlayPause()
  let now = Tone.now()

console.log('ok')

  
  if (isPlaying.value === true) {
     Tone.Transport.start(now )
     sequence.start(now)
  } else {
    
     Tone.Transport.stop()
     sequence.stop()
  }
}

const onKeyDown = (event) => {
  if (event.code === 'Space') {
    togglePlay()
    event.preventDefault()
  }
}

onMounted(() => {
  window.addEventListener('keydown', onKeyDown)
})

onUnmounted(() => {
  window.removeEventListener('keydown', onKeyDown)
